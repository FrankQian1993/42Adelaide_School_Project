#include <unistd.h>
#include <stdio.h>

int	isalpha(int c)
{
	if (c >= 'A' && c <= 'Z' 
			|| c >= 'a' && c <= 'z')
	{
		return (1);
	}
	return (0);
}

int	main()
{
	char	a;
	int		b;
	a = 'A';
	b = isalpha(a);
	printf("%d",b);
	return (0);
}

