#include <stdio.h>

int	isdigit(int c)
{
	if (c >= '0' && c<= '9')
	{
		return (1);
	}
	return (0);
}

int main()
{
	char	a;
	char	b;

	a = 'A';
	b = '9';
	printf("%d\n",isdigit(a));
	printf("%d\n",isdigit(b));
	return (0);
}
